  <h3 class="h3 mt-md-1">About IUCEE</h3>
  <div class="container pt-4 col-md-11 justify-content-center">

    <p>Indo Universal Collaboration for Engineering Education (IUCEE) was conceptualized by over 150 leaders of engineering education and businesses from US and India in 2007. The name was modified in 2016 from Indo US Collaboration for Engineering Education to reflect the more global nature of the collaborations. IUCEE is one of the founder members of IFEES (International Federation for Engineering Education Societies), which was the primary facilitator for the growth and development of IUCEE.</p>
      <p>The vision of IUCEE is to improve the quality and global relevance of engineering education and research in India with related benefits to engineering educators around the world. The major
    focus is on faculty development, student development, curriculum development, as well as improved teaching technologies & engineering education research.</p>
    <p>The IUCEE process is under way during the past ten years. Financial contributions from distinguished patrons helped initiate the program. International experts from the US travel to India to discuss global best practices in teaching and research in their field with Indian faculty at Faculty Leadership Institutes. During the past ten years, 180 workshops were conducted. Significant positive outcomes from these workshops have been demonstrated. Networks for collaborations have resulted. Faculty from over 100 Indian colleges and 200 international faculty members from over 50 international institutions around the world, are connected in a wide variety of research and teaching collaborations.</p>
    <p>IUCEE has become established as a prime mover for improving quality of engineering education in more than 50 engineering colleges in India. An ecosystem has been built with the help of global experts from around the world. This ecosystem includes face-to-face workshops, webinars and courses in a blended format for faculty development, international conferences, a peer-reviewed journal for engineering education, a certification program as well as networking opportunities between engineering educators and students from India and the IFEES global community.</p>
